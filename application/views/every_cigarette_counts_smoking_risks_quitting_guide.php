                        <h2>What's Inside a Cigarette? (Composition)</h2>
                        <p>We all know smoking is harmful-but have you ever thought about what's really in that single puff that quietly takes control of us? A cigarette may seem like just “tobacco rolled in paper,” but in reality, it's a chemical cocktail- having the power to addict you, affect your health, and quietly harm your body over time. Cigarettes have around 600 ingredients that produce over 7,000 chemicals when burned-at least 69 of which can cause cancer. Before we dive deeper into how smoking affects your lungs and heart, it's important to understand what's in a cigarette.</p>
                        <p>Some of the most harmful substances include:</p>
                        <ul>
                            <li><strong>Nicotine - </strong>You've probably heard the name, but do you know what it really is? Nicotine is highly addictive; affects your brain and makes you crave the next puff almost automatically. </li>
                            <li><strong>Tar-A </strong>thick, black residue that builds up in the lungs, making breathing difficult and increasing the risk of respiratory infections and other lung-related illnesses. </li>
                            <li><strong>Carbon monoxide - </strong>the same gas found in car exhaust. In cigarette smoke, it's poisonous, cutting the oxygen in your blood and forcing your heart to work harder.</li>
                            <li><strong>Heavy metals (lead, arsenic, cadmium) - </strong>these toxic substances in cigarette smoke gradually damage your bones, kidneys, and nerves, often without obvious signs at first. </li>
                            <li><strong>Carcinogens - </strong>every puff exposes you to over 70 cancer-causing chemicals, quietly putting your health and your life at risk. </li>
                        </ul>
                        <p>Cigarettes don't just provide a temporary escape-they act as a slow-acting poison, quietly harming your organs and weakening your heart, lungs, bones, and immune system, while increasing your long-term risk of lung cancer, heart disease, stroke. </p>
                        <h2>The Health Risks of Smoking</h2>
                        <p>When you think of smoking, lung cancer might be the first thing that comes to mind-but the risks go much further. Smoking also causes chronic respiratory problems like COPD, emphysema, and bronchitis, along with many smoking-related diseases. The damage doesn't happen overnight-it builds silently, puff by puff, day by day. </p>
                        <p>Let's see how smoking harms two of your most vital organs: the lungs and the heart.</p>
                        <h2>Every Puff Counts: The Link Between Smoking and Lung Cancer </h2>
                        <p>Smoking is the leading cause of lung cancer, accounting for nearly 80% of cases. Each cigarette delivers hundreds of cancer-causing chemicals straight to your lungs, harming cells and DNA long before symptoms appear. But the danger doesn't stop there, smoking also raises the risk of cancers in your mouth, throat, esophagus, bladder, and pancreas.</p>
