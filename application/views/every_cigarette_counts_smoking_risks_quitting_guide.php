                        <h3>Smoking and Your Heart: The Silent Strain</h3>
                        <p>Cigarette smoke doesn't just irritate your lungs-it quietly targets your heart. Every chemical you inhale narrows blood vessels, raises blood pressure, and forces your heart to work overtime. The effects are often invisible at first, but over time, they can lead to serious conditions like heart disease, heart attacks, and strokes.</p>
                        <h3>Not a Smoker? You're Still at Risk- Secondhand smoking (Passive Smoking)</h3>
                        <p>Think you're safe because you don't smoke? Think again. Secondhand smoke is a mix of what a smoker exhales and the smoke from the burning cigarette, packed with harmful chemicals that can quietly harm anyone nearby.  </p>
                        <p>Research shows that nonsmokers exposed to secondhand smoke have a 20-30% higher risk of developing lung cancer and nearly 34,000 heart disease deaths annually in the U.S. alone. Even brief exposure can harm blood vessels and increase the risk of heart attack.</p>
                        <h3>Key points: </h3>
                        <ul>
                            <li>Can trigger asthma, bronchitis, and lung cancer in non-smokers. </li>
                            <li>Children face ear infections, wheezing, and sudden infant death syndrome</li>
                            <li>Pregnant women are at high risk of low birth weight and preterm delivery. </li>                            
                        </ul>
                        <p><strong>So, Take a breath of safety-limit exposure to secondhand smoke today. </strong></p>
