                        <p>For many young people, smoking begins innocently, a cigarette shared with friends, a way to “fit in,” or a moment of social enjoyment. But what starts as a casual experiment quickly becomes a trap. Ideally, nicotine isn't a choice; over time, it quickly takes hold, creating dependency and making it hard to quit before adulthood. This leads to real consequences: weaker lungs, increased risk of heart disease, reduced athletic performance, and even long-term illnesses like cancer. </p>
                        <p>The solution is simple: awareness and support. Turn to sports such as running, swimming, team games or hobbies that energize and uplift you, not habits that harm. Surround yourself with friends who inspire, and let families, schools, and communities guide you to stay strong. </p>
